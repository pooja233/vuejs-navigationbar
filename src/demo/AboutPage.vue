Shantanurao Laxmanrao Kirloskar is our inspiration for his dynamism, perseverance and strong principles. A man of practical thinking, SLK was one of the earliest successful entrepreneurs of modern India.

SLK became the first Indian to graduate from Massachusetts Institute of Technology in 1926. For his immense contribution to trade and industry, he was awarded the Padma Bhushan in 1965; one of the highest Indian civilian awards.

A commemorative postage stamp was released by the Indian Prime Minister marking SLK’s centenary in 2003.

